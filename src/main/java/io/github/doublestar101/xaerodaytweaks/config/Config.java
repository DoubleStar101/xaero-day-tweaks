package io.github.doublestar101.xaerodaytweaks.config;

public class Config {

    /**
     * Enables or disables all modifications made by Xaero Day Tweaks.
     */
    public boolean enabled = true;

    /**
     * Offset applied to Xaero's day counter.
     *
     * 0 = Vanilla Day 0
     * 1 = Original Xaero behavior
     */
    public int dayOffset = 0;
}